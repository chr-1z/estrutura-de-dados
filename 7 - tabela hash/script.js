/*

    #Tabela de Espalhamento

    Uma tabela hash, de dispersão ou espalhamento é uma
    estrutura de dados especial, que associa chaves de 
    pesquisa a valores.



    #Hashing

    Uma tabela hash é uma generalização da idéia de array,
    porém utiliza uma função denominada Hashing para 
    espalhar os elementos, fazendo com que os mesmo fiquem
    de forma não ordenada dentro do "array" que define
    a tabela.

    A tabela hash permite a associação de "valores" a "chaves"

    Valores: é a posição ou índice onde o elemento se encontra

    Chave: parte da informação que compõe o elemento a ser
    manipulado.

    Espalhar facilita a busca na estrutura de dados, pois a
    partir de uma chave podemos acessar de forma rápida uma
    posição do "array".




*/
